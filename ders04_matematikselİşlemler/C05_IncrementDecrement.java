package ders04_matematikselİşlemler;

public class C05_IncrementDecrement {
    public static void main(String[] args) {

        int sayi=10;

        sayi= sayi+3;

        System.out.println(sayi);

        sayi +=3;
        System.out.println(sayi);

        int s=10;
        s=s+1;
        System.out.println("s = " + s);
        s+=1;
        System.out.println(s);
        s++;
        System.out.println(s);
        ++s;
        System.out.println("s = " + s);



    }
}
